def Desc(Textbox):
    match Textbox:
        case "1st_clef":
            return "A clef you want to translate!"
        
        case "major":
            return "A major for your clef!"
        
        case "note":
            return "A note you want for your clef!"
        
        case "octave":
            return "An octave you want for your clef!"
        
        case "2nd_clef":
            return "A clef you want to see your selected clef translated too!"
        