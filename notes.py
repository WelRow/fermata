def Notes(major):
    match major:
        case "A Major":
            return ['A', 'B', 'C#', 'D', 'E', 'F#', 'G#']
        
        case "Bb Major":
            return ['Bb', 'C', 'D', 'Eb', 'F', 'G', 'A']
        
        case "C Major":
            return ['C', 'D', 'E', 'F', 'G', 'A', 'B']
        
        case "D Major":
            return ['D', 'E', 'F#', 'G', 'A', 'B', 'C#']
        
        case "G Major":
            return ['G', 'A', 'B', 'C', 'D', 'E', 'F#']
        
        case "F Major":
            return ['F', 'G', 'A', 'Bb', 'C', 'D', 'E']
        


    

